// Helper function untuk mendapatkan artikel berdasarkan slug
export const getNewsBySlug = (slug) => {
return newsData.find((news) => news.slug === slug);
};

// Helper function untuk mendapatkan artikel berdasarkan id
export const getNewsById = (id) => {
return newsData.find((news) => news.id === id);
};

// Helper function untuk mendapatkan artikel terbaru
export const getLatestNews = (limit = 3) => {
return newsData
.sort((a, b) => new Date(b.date) - new Date(a.date))
.slice(0, limit);
};

// Helper function untuk mendapatkan artikel berdasarkan kategori
export const getNewsByCategory = (category) => {
return newsData.filter((news) => news.category === category);
};